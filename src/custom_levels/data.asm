MACRO levelHeaderRLE pages, horizontal, bgPalette, spritePalette, music
	.db horizontal << 7 | bgPalette << 3 | spritePalette
	.db pages << 4 | music
ENDM
MACRO rleBase cmd, size, y, x
	.db (cmd << 5) + size
	rleYX y, x
ENDM
MACRO rleSimple cmd, size, y, x, tile
	rleBase cmd, size, y, x
	if !(size == 0)
		.db tile
		lastTile = tile
		lastSize = size
	endif
ENDM
MACRO rleYX y, x
	.db (y<<4) + (x)
ENDM
MACRO rlePage page
	.db #$F0 | page
ENDM
MACRO rleChangeRawData my_addr
    ; my_addr = my_addr - 2
	.db #$FE, <my_addr, >my_addr
ENDM
MACRO rleDoorPtr world, level, area, page
	.db $FD, (world*10 + level), (area << 4) + page
ENDM
MACRO rleRepeatNext size
	.db (%111 << 5) + size
ENDM

